#ifndef DT_MACROS_H_
#define DT_MACROS_H_

/*
 * Returns the maximum of the variables A and B.
 */
#define MAX(A, B) ((A) > (B) ? (A) : (B))

/*
 * Returns the minimum of the variables A and B.
 */
#define MIN(A, B) ((A) < (B) ? (A) : (B))

#endif /* DT_MACROS_H_ */
